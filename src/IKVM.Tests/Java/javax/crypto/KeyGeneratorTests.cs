using Microsoft.VisualStudio.TestTools.UnitTesting;

namespace IKVM.Tests.Java.javax.crypto
{

    [TestClass]
    public class KeyGeneratorTests
    {

        [TestMethod]
        public void Can_generate_des_key()
        {
            var keygen = global::javax.crypto.KeyGenerator.getInstance("DES");
            var keyval = keygen.generateKey();
        }

        [TestMethod]
        public void Can_generate_blowfish_key()
        {
            var keygen = global::javax.crypto.KeyGenerator.getInstance("Blowfish");
            var keyval = keygen.generateKey();
        }

        [TestMethod]
        public void Can_generate_aes_key()
        {
            var keygen = global::javax.crypto.KeyGenerator.getInstance("AES");
            keygen.init(128);
            var keyval = keygen.generateKey();
        }

    }

}
